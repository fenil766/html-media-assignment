//  arithmetic opernation

let num1=10
let num2=2

// addition
console.log(` the addition of num1 and num2 is ${num1+num2}`)

// substraction
console.log(`the subtraction of num1 and num2 is ${num1-num2}`)

//  multiplication
console.log(` the multiplication of num1 and num2 is ${num1*num2}`)

// division
console.log(`The division of num1 and num2 is ${num1/num2}`)

// modulus
console.log(`The remainder or modulus of num1 divided by num2 is ${num1%num2}`)

// exponentiation(**)
console.log(`The exponentiation of num1 and num2 is ${num1**num2}`)